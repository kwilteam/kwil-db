package node

import (
	"github.com/kwilteam/kwil-db/core/rpc/transport"
)

// defaultAdminClients is the name of the file containing the PEM-encoded
// certificates or certificate authorities for authorized clients of the admin
// gRPC service. This file will contain a concatenation of all the client TLS
// certificates, which will typically just be one.
const defaultAdminClients = "clients.pem"

// genCertPair generates a key/cert pair to the paths provided.
func genCertPair(certFile, keyFile string, altDNSNames []string) error {
	org := "kwild server autogenerated cert"
	return transport.GenTLSKeyPair(certFile, keyFile, org, altDNSNames)
}
